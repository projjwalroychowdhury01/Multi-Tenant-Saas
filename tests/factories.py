class PlanFactory(DjangoModelFactory):
    """Creates a billing Plan. Defaults to a FREE-tier configuration."""

    class Meta:
        model = "billing.Plan"
        django_get_or_create = ("slug",)

    name = factory.Sequence(lambda n: f"Plan {n}")
    slug = factory.LazyAttribute(lambda o: o.name.lower().replace(" ", "-"))
    price_monthly = "0.00"
    limits = factory.LazyFunction(
        lambda: {
            "members_count": 5,
            "api_calls_per_month": 10_000,
            "storage_mb": 512,
        }
    )
    features = factory.LazyFunction(
        lambda: {
            "audit_logs": False,
            "feature_flags": False,
            "sso": False,
        }
    )
    is_active = True


class SubscriptionFactory(DjangoModelFactory):
    """Creates an active Subscription linking an org to a plan."""

    class Meta:
        model = "billing.Subscription"

    organization = factory.SubFactory(OrganizationFactory)
    plan = factory.SubFactory(PlanFactory)
    status = "active"
    current_period_start = factory.LazyFunction(
        lambda: __import__("django.utils.timezone", fromlist=["now"]).now()
    )
    current_period_end = factory.LazyFunction(
        lambda: __import__("django.utils.timezone", fromlist=["now"]).now()
        + __import__("datetime").timedelta(days=30)
    )


class InvoiceFactory(DjangoModelFactory):
    """Creates a PAID invoice for a subscription."""

    class Meta:
        model = "billing.Invoice"

    subscription = factory.SubFactory(SubscriptionFactory)
    amount_cents = 4900  # $49.00
    status = "paid"
    stripe_invoice_id = factory.LazyFunction(
        lambda: f"mock_inv_{__import__('uuid').uuid4().hex[:12]}"
    )
    period_start = factory.LazyFunction(
        lambda: __import__("django.utils.timezone", fromlist=["now"]).now()
    )
    period_end = factory.LazyFunction(
        lambda: __import__("django.utils.timezone", fromlist=["now"]).now()
        + __import__("datetime").timedelta(days=30)
    )
    paid_at = factory.LazyFunction(
        lambda: __import__("django.utils.timezone", fromlist=["now"]).now()
    )
